#[allow(unused_macros)]
macro_rules! debug {
    ($($a:expr),* $(,)*) => {
        #[cfg(debug_assertions)]
        eprintln!(concat!($("| ", stringify!($a), "={:?} "),*, "|"), $(&$a),*);
    };
}

#[allow(clippy::needless_range_loop)]
#[fastout]
fn main() {
    input! {
        n: usize,
        a: [[usize; 6]; n],
    }

    use ac_library::ModInt1000000007 as Mint;

    let ans = a
        .iter()
        .map(|ai| Mint::new(ai.iter().sum::<usize>()))
        .product::<Mint>();

    println!("{}", ans);
}
