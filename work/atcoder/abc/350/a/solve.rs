#[allow(unused_macros)]
macro_rules! debug {
    ($($a:expr),* $(,)*) => {
        #[cfg(debug_assertions)]
        eprintln!(concat!($("| ", stringify!($a), "={:?} "),*, "|"), $(&$a),*);
    };
}

#[allow(clippy::needless_range_loop)]
#[fastout]
fn main() {
    input! {
        s: String,
    }

    let suffix = s[3..6].parse::<usize>().unwrap();
    let valid = (1..=349).contains(&suffix) && suffix != 316;

    println!("{}", if valid { "Yes" } else { "No" });
}
