#[allow(unused_macros)]
macro_rules! debug {
    ($($a:expr),* $(,)*) => {
        #[cfg(debug_assertions)]
        eprintln!(concat!($("| ", stringify!($a), "={:?} "),*, "|"), $(&$a),*);
    };
}

#[fastout]
fn main() {
    input! {
        s: String,
    }

    let ans = match &s as &str {
        "RRR" => 3,
        "RRS" => 2,
        "SRR" => 2,
        "SSS" => 0,
        _ => 1,
    };

    println!("{}", ans);
}
