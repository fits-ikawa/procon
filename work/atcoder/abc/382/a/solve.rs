#[allow(unused_macros)]
macro_rules! debug {
    ($($a:expr),* $(,)*) => {
        #[cfg(debug_assertions)]
        eprintln!(concat!($("| ", stringify!($a), "={:?} "),*, "|"), $(&$a),*);
    };
}

#[allow(clippy::needless_range_loop)]
#[fastout]
fn main() {
    input! {
        n: usize, d: usize,
        s: Chars,
    }

    let mut count = s.iter().copied().counts();

    count.entry('.').or_insert(0);

    println!("{}", count[&'.'] + d);
}
