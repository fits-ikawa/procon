#[allow(unused_macros)]
macro_rules! debug {
    ($($a:expr),* $(,)*) => {
        #[cfg(debug_assertions)]
        eprintln!(concat!($("| ", stringify!($a), "={:?} "),*, "|"), $(&$a),*);
    };
}

#[allow(clippy::needless_range_loop)]
fn main() {
    input! {
        s: Chars,
    }

    let n = s.len();
    let ap = n / 2;
    let bp = s.iter().filter(|&&c| c == 'p').count();

    println!("{}", ap - bp);
}
