#[allow(unused_macros)]
macro_rules! debug {
    ($($a:expr),* $(,)*) => {
        #[cfg(debug_assertions)]
        eprintln!(concat!($("| ", stringify!($a), "={:?} "),*, "|"), $(&$a),*);
    };
}

#[allow(clippy::needless_range_loop)]
#[fastout]
fn main() {
    input! {
        n: usize, q: usize,
        mut a: [usize; n],
        x: [usize; q],
    }

    a.sort();

    for xi in x {
        let ans = n - a.lower_bound(&xi);
        println!("{}", ans);
    }
}
