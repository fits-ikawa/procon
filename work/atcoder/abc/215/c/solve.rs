use superslice::*;

#[allow(unused_macros)]
macro_rules! debug {
    ($($a:expr),* $(,)*) => {
        #[cfg(debug_assertions)]
        eprintln!(concat!($("| ", stringify!($a), "={:?} "),*, "|"), $(&$a),*);
    };
}

#[allow(clippy::needless_range_loop)]
#[fastout]
fn main() {
    input! {
        mut s: Chars, k: usize,
    }

    s.sort();

    for _ in 0..k - 1 {
        s.next_permutation();
    }

    println!("{}", s.iter().join(""));
}
