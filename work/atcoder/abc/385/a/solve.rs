#[allow(unused_macros)]
macro_rules! debug {
    ($($a:expr),* $(,)*) => {
        #[cfg(debug_assertions)]
        eprintln!(concat!($("| ", stringify!($a), "={:?} "),*, "|"), $(&$a),*);
    };
}

#[allow(clippy::needless_range_loop)]
#[fastout]
fn main() {
    input! {
        a: usize, b: usize, c: usize,
    }

    if (a == b && b == c) || (a + b == c) || (a + c == b) || (a == b + c) {
        println!("Yes");
    } else {
        println!("No");
    }
}
